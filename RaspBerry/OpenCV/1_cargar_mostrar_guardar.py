import cv2

#////////////////////////////////////////////////////////////////////////////////////
# VARIABLES GLOBALES
#////////////////////////////////////////////////////////////////////////////////////

#////////////////////////////////////////////////////////////////////////////////////
# FUNCIONES
#////////////////////////////////////////////////////////////////////////////////////

#////////////////////////////////////////////////////////////////////////////////////
# CONFIGURACIÓN PUERTOS GPIO Y RECURSOS A UTILIZAR
#////////////////////////////////////////////////////////////////////////////////////

#////////////////////////////////////////////////////////////////////////////////////
# PROGRAMA PRINCIPAL
#////////////////////////////////////////////////////////////////////////////////////
try:                 
    while True:      #iniciamos un loop infinito
        #cargamos una imagen de color en modo escala de grises
        img = cv2.imread('cocina.jpg',0)

        #mostramos la imagen
        cv2.imshow('Imagen de cocina',img)

        #esperamos la pulsación de cualquier tecla
        cv2.waitKey(0)
        
        #Guardamos la imagen
        cv2.imwrite('cocina_copy.jpg',img)

        #Salimos del While y pasamos por "finally"
        break       
     

except KeyboardInterrupt:   #Si el usuario pulsa CONTROL+C entonces...
    print("El usuario ha pulsado Ctrl+C...")

except:
    print("error inesperado")

finally:
    cv2.destroyAllWindows()




 
